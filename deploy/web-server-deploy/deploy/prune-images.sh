#!/bin/bash
# Периодическая очистка неиспользуемых Docker-образов на прод-сервере.
# Причина: `do.sh` пушит образы всегда под один и тот же тег (":1"), поэтому каждый
# деплой оставляет предыдущий слой образа "висящим" (untagged) — без регулярной чистки
# диск постепенно заполняется до 100%, из-за чего Postgres перестаёт писать
# ("No space left on device") и весь сайт перестаёт отвечать (инцидент 2026-07-09).
# `docker image prune -a -f` не трогает образы, на которые ссылается хотя бы один
# контейнер (включая остановленные), поэтому безопасен для регулярного запуска.
set -euo pipefail

echo "[$(date '+%Y-%m-%d %H:%M:%S')] Запуск docker image prune"
docker image prune -a -f
echo "[$(date '+%Y-%m-%d %H:%M:%S')] Готово. Свободно на диске:"
df -h / | tail -n +2
