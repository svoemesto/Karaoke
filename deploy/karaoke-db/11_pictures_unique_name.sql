-- Уникальность имени картинки в tbl_pictures.
--
-- Проблема: pictureAlbum/pictureAuthor-геттеры (Settings.kt) создают запись картинки
-- через Pictures.createNewPicture(), который сначала ищет запись по имени, а если не
-- нашёл — вставляет. При параллельной обработке нескольких песен ОДНОГО альбома
-- (общий album-логотип) две задачи одновременно не находят запись и обе делают INSERT
-- → дубли строк с одинаковым picture_name. У tbl_pictures не было уникального индекса,
-- поэтому дубли накапливались (на момент ввода — 49 дублирующихся имён).
--
-- Решение: уникальный индекс на picture_name (DB-уровневая защита) + обработка гонки
-- в коде (Pictures.createNewPicture ловит нарушение уникальности и возвращает уже
-- вставленную соседним потоком запись). Дубли безопасны для чистки: одинаковое имя →
-- одинаковый ключ в MinIO, оставшаяся запись ссылается на тот же файл.
--
-- ВАЖНО: перед созданием индекса таблица должна быть очищена от дублей (иначе CREATE
-- UNIQUE INDEX упадёт). Разовая чистка (оставить строку с минимальным id на каждое имя):
--   DELETE FROM tbl_pictures a USING tbl_pictures b
--   WHERE a.picture_name = b.picture_name AND a.id > b.id;
-- Применять на LOCAL и SERVER отдельно (миграция сама на сервер не попадает).

CREATE UNIQUE INDEX IF NOT EXISTS uq_tbl_pictures_picture_name ON tbl_pictures (picture_name);
